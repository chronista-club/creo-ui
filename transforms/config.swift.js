/**
 * Style Dictionary config for Swift (SPM) platform.
 *
 * Generates SwiftUI Color / CGFloat extensions from W3C DTCG tokens.
 * Consumed by CreoUI SPM package (packages/swift).
 *
 * Phase 1 design notes:
 *   - Output SwiftUI `Color` (cross-platform: iOS / macOS / watchOS / tvOS)
 *     instead of UIColor (iOS-only).
 *   - Emit tokens as a `public extension Color` for brand/semantic colors,
 *     and a `public enum CreoUITokens` namespace holding CGFloat / String
 *     dimensions and typography tokens.
 *   - Keep only minimal, hand-rolled formats — matches the DTCG `$value`
 *     shape directly instead of relying on Style Dictionary's iOS transform
 *     group (which targets UIKit).
 */

const camelCase = (path) =>
  path
    .map((seg, i) => {
      const parts = seg.split(/[-_]/g)
      return parts
        .map((p, j) => {
          if (i === 0 && j === 0) return p.toLowerCase()
          return p.charAt(0).toUpperCase() + p.slice(1).toLowerCase()
        })
        .join('')
    })
    .join('')

const sanitizeIdent = (name) => {
  if (/^[0-9]/.test(name)) return `_${name}`
  return name
}

const hexToRgb = (hex) => {
  const h = hex.replace('#', '')
  const v =
    h.length === 3
      ? h
          .split('')
          .map((c) => c + c)
          .join('')
      : h
  return {
    r: Number.parseInt(v.slice(0, 2), 16) / 255,
    g: Number.parseInt(v.slice(2, 4), 16) / 255,
    b: Number.parseInt(v.slice(4, 6), 16) / 255,
  }
}

const dimensionToFloat = (value) => {
  if (typeof value === 'number') return value
  const match = String(value).match(/^(-?\d*\.?\d+)(px|pt|rem)?$/)
  if (!match) return null
  const n = Number.parseFloat(match[1])
  const unit = match[2] ?? 'px'
  if (unit === 'rem') return n * 16
  return n
}

export default {
  source: ['tokens/**/*.json'],
  hooks: {
    formats: {
      'swift/creo-ui': ({ dictionary }) => {
        const header = [
          '//',
          '// Tokens.swift',
          '//',
          '// Auto-generated by Style Dictionary from Creo UI tokens.',
          '// Do not edit manually — edit tokens/**/*.json instead.',
          '//',
          '',
          'import SwiftUI',
          'import CoreGraphics',
          '',
        ].join('\n')

        const colors = []
        const dimensions = []
        const numbers = []
        const strings = []

        for (const token of dictionary.allTokens) {
          // Dark theme variant は現状 Web 限定 (CREO-#1)。Swift は Phase 3 で
          // Color(dynamicProvider:) に対応予定なので、dark path は skip する。
          if (token.path.includes('dark')) continue

          const name = sanitizeIdent(camelCase(token.path))
          const type = token.$type ?? token.type
          const raw = token.$value ?? token.value
          const comment = token.$description ? ` // ${token.$description}` : ''

          if (type === 'color') {
            const { r, g, b } = hexToRgb(String(raw))
            colors.push(
              `    static let ${name} = Color(red: ${r.toFixed(4)}, green: ${g.toFixed(
                4,
              )}, blue: ${b.toFixed(4)})${comment}`,
            )
            continue
          }
          if (type === 'dimension') {
            const n = dimensionToFloat(raw)
            if (n !== null) {
              dimensions.push(`    public static let ${name}: CGFloat = ${n}${comment}`)
              continue
            }
          }
          if (type === 'fontWeight' || type === 'number') {
            numbers.push(`    public static let ${name}: Double = ${Number(raw)}${comment}`)
            continue
          }
          // fontFamily / shadow / fallback — emit as String
          strings.push(
            `    public static let ${name}: String = ${JSON.stringify(String(raw))}${comment}`,
          )
        }

        const sections = [
          'public extension Color {',
          ...colors,
          '}',
          '',
          'public enum CreoUITokens {',
          ...dimensions,
          ...(dimensions.length && numbers.length ? [''] : []),
          ...numbers,
          ...((dimensions.length || numbers.length) && strings.length ? [''] : []),
          ...strings,
          '}',
          '',
        ].join('\n')

        return `${header}\n${sections}`
      },
    },
  },
  platforms: {
    swift: {
      // Apply `name/camel` so each token gets a unique camelCase `name`
      // derived from its full path. This silences Style Dictionary's
      // "token collision" warning (which looks at `token.name`, not the
      // path-based identifier the custom format emits).
      transforms: ['name/camel'],
      buildPath: 'packages/swift/Sources/CreoUI/Generated/',
      files: [
        {
          destination: 'Tokens.swift',
          format: 'swift/creo-ui',
        },
      ],
    },
  },
}
