/**
 * Style Dictionary config for Rust platform.
 *
 * Generates Rust const definitions from W3C DTCG tokens.
 * Consumed by creo-ui crate (packages/rust).
 *
 * Phase 1 design notes:
 *   - Colors are emitted as `Rgb { r, g, b }` ([u8; 3]-backed struct) so
 *     consumers (ratatui, etc.) can convert them without a parse step.
 *   - Dimensions are emitted as `f32` (px units).
 *   - Font weights and plain numbers are emitted as `f32`.
 *   - Font families, shadows, and other string-shaped values are emitted as
 *     `&'static str`.
 *   - No built-in Rust format exists in Style Dictionary; we hand-roll one.
 */

const SCREAMING_SNAKE = (path) =>
  path
    .flatMap((seg) => seg.split(/[-_]/g))
    .map((seg) => seg.toUpperCase())
    .join('_')

const sanitizeIdent = (name) => {
  // Rust idents cannot start with a digit — prefix with `_`.
  if (/^[0-9]/.test(name)) return `_${name}`
  return name
}

const hexToRgbBytes = (hex) => {
  const h = hex.replace('#', '')
  const v =
    h.length === 3
      ? h
          .split('')
          .map((c) => c + c)
          .join('')
      : h
  return {
    r: Number.parseInt(v.slice(0, 2), 16),
    g: Number.parseInt(v.slice(2, 4), 16),
    b: Number.parseInt(v.slice(4, 6), 16),
  }
}

const dimensionToFloat = (value) => {
  if (typeof value === 'number') return value
  const match = String(value).match(/^(-?\d*\.?\d+)(px|pt|rem)?$/)
  if (!match) return null
  const n = Number.parseFloat(match[1])
  const unit = match[2] ?? 'px'
  if (unit === 'rem') return n * 16
  return n
}

export default {
  source: ['tokens/**/*.json'],
  hooks: {
    formats: {
      'rust/creo-ui': ({ dictionary }) => {
        // NOTE: this file is intended to be `include!()`ed into an
        // outer module, so we avoid inner doc comments (`//!`) and inner
        // attributes (`#![...]`) — those are only allowed at the top of a
        // module / crate, not in the middle of one.
        const header = [
          '// Auto-generated by Style Dictionary from Creo UI tokens.',
          '// Do not edit manually — edit tokens/**/*.json instead.',
          '',
          '/// 8-bit-per-channel RGB color, suitable for conversion into',
          '/// `ratatui::style::Color::Rgb`, `image::Rgb`, etc.',
          '#[derive(Debug, Clone, Copy, PartialEq, Eq)]',
          'pub struct Rgb {',
          '    pub r: u8,',
          '    pub g: u8,',
          '    pub b: u8,',
          '}',
          '',
          'impl Rgb {',
          '    pub const fn new(r: u8, g: u8, b: u8) -> Self {',
          '        Self { r, g, b }',
          '    }',
          '',
          '    pub const fn as_array(&self) -> [u8; 3] {',
          '        [self.r, self.g, self.b]',
          '    }',
          '}',
          '',
        ].join('\n')

        const lines = []
        for (const token of dictionary.allTokens) {
          const name = sanitizeIdent(SCREAMING_SNAKE(token.path))
          const type = token.$type ?? token.type
          const raw = token.$value ?? token.value
          const comment = token.$description
            ? `/// ${String(token.$description).replace(/\n/g, ' ')}\n`
            : ''

          if (type === 'color') {
            const { r, g, b } = hexToRgbBytes(String(raw))
            lines.push(`${comment}pub const ${name}: Rgb = Rgb::new(${r}, ${g}, ${b});`)
            continue
          }
          if (type === 'dimension') {
            const n = dimensionToFloat(raw)
            if (n !== null) {
              lines.push(`${comment}pub const ${name}: f32 = ${n}_f32;`)
              continue
            }
          }
          if (type === 'fontWeight' || type === 'number') {
            lines.push(`${comment}pub const ${name}: f32 = ${Number(raw)}_f32;`)
            continue
          }
          lines.push(`${comment}pub const ${name}: &str = ${JSON.stringify(String(raw))};`)
        }

        return `${header}\n${lines.join('\n')}\n`
      },
    },
  },
  platforms: {
    rust: {
      // Apply `name/constant` so each token gets a unique SCREAMING_SNAKE
      // `name` derived from its full path. This silences Style Dictionary's
      // "token collision" warning (which looks at `token.name`, not the
      // path-based identifier the custom format emits).
      transforms: ['name/constant'],
      buildPath: 'packages/rust/src/generated/',
      files: [
        {
          destination: 'tokens.rs',
          format: 'rust/creo-ui',
        },
      ],
    },
  },
}
